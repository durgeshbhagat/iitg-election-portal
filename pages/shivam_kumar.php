						<h3>Shivam Kumar (140103063)</h3>
						<p class="nihil">Tagline: HAM HONGE KAMYAB</p><br></br>
	<!---728x90--->
						<p>"Agendas: Change is the word that can draw attention towards the crowd. I believe that the change is imminent. However, we need a force to trigger that change and I firmly believe that I can be the change people want to see. My motto will be to provide best possible environment for the all-round development of the entire IIT Guwahati community."</p>
						<p><br><b>The following points describe the course of action that I wish to adopt to ensure the fulfillment of my promises.</b><br></br></p>
							<p style="color:#df1728;font-size:20px;">GENERAL CAMPUS LIFE</p><br>
							<p style="color:#7480B7;font-size:16px;">1. Married Scholars Hostel</p>
							<p>	    
								<span style="color:#7480B7;">&#8226;</span> Allotment of hostel fund to married scholars’ hostel. Currently, they are not provided with a hostel fund because of which they are facing issues while participating in the events like Spardha, Manthan, etc.<br>
								<span style="color:#7480B7;">&#8226;</span> Provisions for the treatment of the family members of married scholars; non denial of medical facilities to their parents.<br>
								<span style="color:#7480B7;">&#8226;</span> Provisions for insurance for the spouse and children of married scholars.<br>
							</p> <br>
							<p style="color:#7480B7;font-size:16px;">2.	Issues related to safety of women</p> 
							<p>	    
								<span style="color:#7480B7;">&#8226;</span> Presence of female security guards near research labs, etc at least until 12 mid-night. <br>
								<span style="color:#7480B7;">&#8226;</span> Increasing the number of guards during fests. <br>
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3.	Issues related to the hospital</p> 
							<p>	    
								<span style="color:#7480B7;">&#8226;</span> Installation of a hospital canteen that would be able to provide food for the patients as per their medical requirements as at present, the food is brought from Subhansiri hostel, which is not cooked as, prescribed for a patient.<br>
								<span style="color:#7480B7;">&#8226;</span> X-ray machines will be installed as per the requirements because as of now, the students have to rush to GNRC for immediate check up.<br>
								<span style="color:#7480B7;">&#8226;</span> The hospital must possess vaccine for rabies as well as anti-venom for snakebites.<br>
								<span style="color:#7480B7;">&#8226;</span> Equipping the hospital with basic amenities like plastering capabilities, etc.<br>
								<span style="color:#7480B7;">&#8226;</span> The entire population(workers and staff) of the campus must be allowed to access the hospital services. <br>
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4.	For the junta</p> 
							<p>	    
								<span style="color:#7480B7;">&#8226;</span> 24/7 study room to create a proper atmosphere of students during exam preparation. This would also act as a substitute for the library during the duration of exams as it is usually packed.<br>
								<span style="color:#7480B7;">&#8226;</span> Proposal for the representation of foreign students in the Students’ Senate to address their problems more effectively as their problems are going unnoticed right now due to the lack of representation.<br>
								<span style="color:#7480B7;">&#8226;</span> Changes in the scheduling and running of buses:<br>
								i. There should be more number of bus stops for the buses connecting the campus and the city.<br>
								ii. The frequency and number of buses plying to the city should be increased, especially on weekends to cater to the needs of the junta.<br>
								iii. Live tracking of IITG buses.<br>
								iv. The number of intra campus buses should be increased during exam hours to cater to the needs of the junta.<br>
								<span style="color:#7480B7;">&#8226;</span> Ladies toilets must be built in all hostels near the reception as there are no provisions for the same in most of the hostels, which causes inconvenience to the junta.<br>
								<span style="color:#7480B7;">&#8226;</span> The contracts of mess, stationery or juice centers of hostels must cancelled in case of  non confidence displayed by more than 2/3rd of the residents of the hostel.<br>
								<span style="color:#7480B7;">&#8226;</span> Establishment of a recreation hall which will contain music system, magazines, novels, etc.<br>
								<span style="color:#7480B7;">&#8226;</span> Establishment of a souvenir shop in an appropriate place in the campus that also has a provision to sell merchandise designed by the students.<br>
								<span style="color:#7480B7;">&#8226;</span> Establishment of a mobile sales and repair shop in the campus for easy access to the facility for the students.<br>
								<span style="color:#7480B7;">&#8226;</span> Special funds for the maintenance of older hostels. This is required for the repair and renovation of tanks, cracks, etc.<br>
								<span style="color:#7480B7;">&#8226;</span> Cleaning of the lake behind the library and the drainage system of the campus. Due to the clogging of the drainage systems, the problem of mosquitoes is increasing day by day.<br>
								<span style="color:#7480B7;">&#8226;</span> Proper utilization of the Dihing basketball court and installation of lights to make it usable after sunset.<br>
								<span style="color:#7480B7;">&#8226;</span> Regular maintenance of street lights that has been recently renewed.<br>
								<span style="color:#7480B7;">&#8226;</span> Immediate checking of drinking water sources in the campus to test for various different harmful chemicals and bacteria.<br>
								<span style="color:#7480B7;">&#8226;</span> Increasing the frequency of general body meetings like ‘Aap ki Sabha’ for an increased interaction between the junta and the administration.<br>
							</p><br>
							<p style="color:#7480B7;font-size:16px;">5.	Academics</p>
							<p>
							    <span style="color:#7480B7;">&#8226;</span> Floating of industry oriented courses to provide increased exposure to students.<br>
								<span style="color:#7480B7;">&#8226;</span> Organizing soft-skills development courses and spoken English courses to improve communication and technical writing skills, which are crucial for placements as well as the future of students.<br>
								<span style="color:#7480B7;">&#8226;</span> Introduction of entrepreneurship courses as a minor and electives.<br>
								<span style="color:#7480B7;">&#8226;</span>	Workshop on patenting process for research oriented students.<br>
								<span style="color:#7480B7;">&#8226;</span>	Regular meeting of the students and the DUPC of the department regarding the course structure so as to keep the courses relevant and convenient for the students.<br>
								<span style="color:#7480B7;">&#8226;</span>	Creation of a database of all research activities in campus, both ongoing and completed for easier access to the students and to create ease in gathering information and contacting the appropriate parties.<br>
								<span style="color:#7480B7;">&#8226;</span>	Creation of study groups for students preparing for various exams like GATE, UPSC, etc to create a forum for discussion about the same. This would also mean sharing of study materials.<br>
							</p><br>
							<p style="color:#7480B7;font-size:16px;">6.	Sports</p>
							<p>
							    <span style="color:#7480B7;">&#8226;</span>	Maintenance of the hockey field near Siang so that it remains usable throughout the year.<br>
								<span style="color:#7480B7;">&#8226;</span>	Maintenance of the floodlights in the athletics ground as well as the levelling of the athletics ground as the surface is too hard and not level due to which the athletes are facing injuries.<br>
								<span style="color:#7480B7;">&#8226;</span>	Providing proper and soft surfaces for weightlifting.<br>
								<span style="color:#7480B7;">&#8226;</span>	Provide proper diet to the Inter-IIT contingent.<br>
								<span style="color:#7480B7;">&#8226;</span>	Provide quality tracksuit and jersey to the Inter-IIT contingent.<br>
						        <span style="color:#7480B7;">&#8226;</span>	Sportsperson of the year: to encourage talent in the campus.<br>
							</p><br>
