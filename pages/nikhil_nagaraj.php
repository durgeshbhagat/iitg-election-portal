							<p style="color:#7480B7;font-size:16px;">1. PhD students Orientation</p>
							<p>
							    Since many Ph.D. students admitted during both semesters (summer and winter) are usually unaware of facilities being provided or features across various laboratories, the various exchange opportunities available a special orientation session shall be held for them. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2. Best TA Award</p>
							<p>
							    Awarding three TAs per department for exemplary work, based on student feedback. Thereby creating incentives for good teaching by the TAs.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. Internet 24x7 for PhD students</p>
							<p>
							    Since PhDs and Research Scholars have need for more information, they need access to Internet, 24x7, so that they can have access to required research material all the time.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4. Conference Calendar</p>
							<p>
							    Compiling a calendar with the various conferences to be held throughout the year, allowing research students to plan their semester in advance.<br>
								
