						<p>"Agendas: Heading the Student Gymkhana Council and the Student Senate, the Vice President, SGC happens to be a very critical position. It acts as a critical point of conversation between the students and the administration, acting as the representative for the former and the mediator between the two."</p>
						<p><br><b>The Vice President of the SGC is responsible for representing the students fairly and justly as well as ensuring that the students on campus have a comfortable stay. Given my credentials and my vision, I believe I can be a viable candidate for the same.</b><br></br></p>
							<p style="color:#df1728;font-size:20px;">Time for a change: Campus Junta</p><br>
							<p style="color:#7480B7;font-size:16px;">1. 24x7 Printing Facilities </p>
							<p>	    
								A 24x7 network based printing facility would be a boon to the campus students. It is a requirement when important documents which are completed late night are to be printed, and that too from the comfort of individual rooms. <br>
								
							</p> <br>
							<p style="color:#7480B7;font-size:16px;">2. Laundry and Washing System</p> 
							<p>	    
								A centralised laundry system for each hostel, so that much of a studentâ€™s time which is wasted standing in the line/waiting for a washing machine, can be utilised for better purposes. It can also be implemented as a single system for the whole campus. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. Internet from 12 noon - 2pm</p> 
							<p>	    
								Since many students have a lab after this slot, this lunch time period can help them prepare for the lab. It can also be used to analyse topics in detail after classes. A complete prohibition of internet for the whole of the working hours creates a lot of inconvenience.<br>
							
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4. Open Cafeteria</p> 
							<p>	    
								An Open Cafeteria to be constructed for encouraging faculty and student interaction, inter-departmental gatherings. It would give students an impetus to get out of the room and socialize. <br>
								
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">5. E-Toilets/Portable Toilets</p>
							<p>
							    Placing portable, automatic cleaning toilets at remote locations throughout the campus especially at the entry gates.<br>
                            </p><br>
							<p style="color:#7480B7;font-size:16px;">6. Life Skills Courses</p>
							<p>
							    Along with the expansion of the new Life Skill courses, organize group discussions, personal interviews and counselling sessions, in collaboration with hostel HMC and Welfare Board, assisting in complete development of the students. It is necessary in today's world to possess soft skills rather than just intellectual proficiency for success, EQ is as important as IQ. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">7.	Public Benches/Picnic Spots</p>
							<p>
							    Placing benches under shade at various scenic spots to sit, relax and hang out with their friends and relish the beauty of the campus. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">8. Spectacle Shop</p>
							<p>
							    Getting a spectacle shop on the campus. Many students have impaired vision for most practical purposes and not having a shop for replacing damaged spectacles makes it quite inconvenient for not only students, but all campus residents. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">9.	Vending Machines</p>
							<p>
							    Placing vending machines at various places on the campus in order to assist people in obtaining snacks and soft drinks at various hours of the day. <br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Academic Life</p><br>
							<p style="color:#7480B7;font-size:16px;">1. More Electives for 2nd Year Students</p>
							<p>
							    As a redesign of the curriculum is going on, initiatives to introduce more elective courses from the second year onwards shall be proposed so that the students are able to gain a holistic perspective about their future options. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	E-Books packages for Freshers</p>
							<p>
							    Providing incoming freshers with a package containing e-books for all their courses in the first year so as to enable them to study without any hassles. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3.	Database for Lab Facilities</p>
							<p>
							    Compiling a database including all the facilities available in various labs across all the departments. This shall act as a one stop portal to figure out the availability of different instruments/research facilities throughout the institute. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4. Sports Scholarships</p>
							<p>
							    Providing exemplary sportsmen scholarships so as to encourage them to pursue sports and also reduce any financial difficulties that they might face. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">5. Scholarship calculator</p>
							<p>
							    Allowing students to look into the various scholarships they can tap into and allowing them to calculate the best amount of scholarship they can get. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">6.	New SA Courses </p>
							<p>
							    Introduce new SA courses for western musical instruments like guitar, keyboard etc <br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Hostel Life</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	On time maintenance for Hostels</p>
							<p>
							    By creation of a separate maintenance cell for hostels, we will ensure on time maintenance of hostel facilities. Immediate redressal of issues such as faulty washing machines, water purifiers, coolers, internet ports and electrical appliances in rooms, and basic infrastructure. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	Iron Stations in Hostels</p>
							<p>
							    Since ironing and heater facilities are not allowed in hostel rooms, iron stations can be set in all hostels to allow students to iron their clothes. This facility can be used by making an entry at the guard's desk. <br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: New SAC</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	RFID for Club Rooms</p>
							<p>
							    By using RFID enabled electromagnetic locks for club room access, we can streamline and make the process of utilising the New SAC rooms easier.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2. Dairy</p>
							<p>
							    Although the proposal for a dairy has been approved, I will ensure that this process is completed at the earliest.<br>
								
							</p><br>
							
						    <p style="color:#df1728;font-size:20px;">Time for a change: Campus Infrastructure</p><br>
							<p style="color:#7480B7;font-size:16px;">1. Dihing Basketball Court</p>
							<p>
							    Ensure proper maintenance and lights in the Dihing basketball court.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2. Solar-Powered Street Lights</p>
							<p>
							    In order to reduce electricity consumption and ensure proper lighting of roads making it safer for people to travel, solar powered street lights shall be introduced on campus.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. 2nd Floor of New SAC</p>
							<p>
							    Providing rooms to clubs, which do not currently have rooms, and also creation of a place where people can both work and refresh themselves when they are burnt out.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Cultural Activities</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Portals for P2P Learning</p>
							<p>
							    Creation of online portal for peer to peer learning ,to enable people who know an art to teach the people who want to learn it, by providing meritorious benefits to both the participants.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	Performances by Indian and NE Artists </p>
							<p>
							    Organising performances by Indian and North Eastern artists under the aegis of the cultural board independent of the other fests on campus.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. New Music Room</p>
							<p>
							    Creation of a new music room in order to facilitate the participation of students in music related activities. Currently the lack of a music room impedes practice during events.<br>
								
							</p><br>
												<p style="color:#7480B7;font-size:16px;">4.	Expansion and Transparency of CBS</p>
							<p>
							    Ensuring accountability and transparency in the running of CBS as it is an organization which is becoming a popular medium for content sharing. Expand CBS for general campus usage as it currently is not telecasting events that can and should be telecasted.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">5.	Books/Kindles for New SAC</p>
							<p>
							    Ensure that New SAC has a collection of novels and non-academic books or kindles so that people who can read, discuss and ideate over a cup of coffee.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Sports Activities</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Women Specific Events like Pinkathon</p>
							<p>
							    Incorporating women specific events like Pinkathon etc, so as to raise women related awareness across the campus<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	NSO for Chess</p>
							<p>
							    Since chess is a game of both mental and physical fitness and also with the recent formation of the chess club, NSO for chess should be included.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. Juice Centre in Old SAC</p>
							<p>
							    Since players are hitting it hard in the sports arenas, it becomes vital if some source of refreshment is available close at hand. So we would insure the presence of a juice centre in Old SAC.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4.	Sports Medal</p>
							<p>
							    Awarding 'Sportsman of the Year' and 'Sportsman of the Batch' trophies to imbibe sporting culture among the people.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Technical Facilities</p><br>
							<p style="color:#7480B7;font-size:16px;">1. Portal for issuing computing capabilities</p>
							<p>
							    Providing students with a user-friendly portal for some extra juice (computing capabilities) if needed for high-end processing and running heavy memory-engaging programs.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	Ideation Room</p>
							<p>
							    Creating an ideation room in the concept of 4i labs, allowing budding start-ups/innovators a workplace to incubate and ideate.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Student Welfare</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Ambulance Usage</p>
							<p>
							    It has been seen that during times of emergency, many a times the ambulances are not available due to being out in the city or fetching other patients. By providing a few more ambulances and ensuring their proper usage, we plan to reduce this menace.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	Mosquito and Disease Control</p>
							<p>
							    Ensuring green areas are treated properly in order to minimize mosquitoes and other vectors spreading diseases. The number of mosquitoes in the campus is alarming which is nothing short of a health hazard. Raising the health standards is an immediate must.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">3. Lost and Found Corner</p>
							<p>
							    Setting up a lost and found corner in the Admin building or New SAC, for easy location and finding of lost items The corner would provide a more organised and structured way for the campus residents to deal with lost and found items.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">4. Career Counselling</p>
							<p>
							    Students at the institute have great aspirations for their careers. But they also have many questions and queries that have to be answered. Providing students with career counselling, helping the students decide what's best for them will be a great help for them.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">5. Lounge Rooms in/near the academic complex</p>
							<p>
							    Creating lounge rooms for students actively involved in research work, so that they could take a chill pill for some time and then come back full of energy.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Empowering Senate and Gymkhana</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Finance Committee</p>
							<p>
							    Setting up a committee to look after the finances of the student senate expenditure and ensuring no defaulting of bills. The finance committee shall also be educated in all the norms followed by the finance and accounts section so as to smoothen the process of exchange of money between clubs and administration.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2. Chair Senate</p>
							<p>
							    A member would be appointed to head the Senate council to take updates, scrutinize and guide the working Senate. This would streamline the working and improve efficiency of functioning of the senate leading to better end results.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: Security and Safety</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Define Securityâ€™s role on campus</p>
							<p>
							    Creating a proper set of rules to ensure appropriate conduct of security officers on campus. Their strength if channelled in the right manner can keep the campus safe without sacrificing the freedom/privacy of students.<br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2. Cycle and Laptop Insurance</p>
							<p>
							    Cycles and laptops are the two most valuable assets of an IITG students and especially cycles are really prone to theft. Hence, students would be insured in case of theft of cycles.<br>
								
							</p><br>
							<p style="color:#df1728;font-size:20px;">Time for a change: International Affairs</p><br>
							<p style="color:#7480B7;font-size:16px;">1.	Exchange Students and Cultures</p>
							<p>
							    Teaming up with welfare board, we would let the international exchange students learn more about their rights and responsibilities in and around the campus. There will be sessions organised for the international students to share their stories, their food and their culture with the students of IITG and vice versa. <br>
								
							</p><br>
							<p style="color:#7480B7;font-size:16px;">2.	SAIL-IGAIN</p>
