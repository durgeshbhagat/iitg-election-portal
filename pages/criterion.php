                        <h3>Eligibility Criteria for Contestants</h3>
                        <div class="wlcome-grid-left-grid">
                           <div class="col-xs-12 wlcome-grid-left-grid">
                              <p><br><b>Candidates contesting for Gymkhana Election, year 2017-18 should satisfy the following criteria:</b><br></br>
							    <span style="color:green;">&#10003;</span> He/she must be a bonafide student of IIT Guwahati.<br>
								<span style="color:green;">&#10003;</span> He/she must have registered for at least 2 semesters and should have at least 2 semesters of stay remaining. Also the contestant must have been a resident of IIT Guwahati Campus during his/her tenure.<br>
								<span style="color:green;">&#10003;</span> He/she should have a CPI greater than 6.0 in case of UG and 6.5 in case of PG.<br>
								<span style="color:green;">&#10003;</span> He/she must not have any active backlog at the time of contesting. F grade in minor course or additional learning will not be considered an active backlog.<br>
								<span style="color:green;">&#10003;</span> A dropped compulsory course shall be considered an active backlog.<br>
								<span style="color:green;">&#10003;</span> He/she should have cleared the minimum prescribed credits constituting of core courses, institute electives and departmental electives at the time of contesting.<br>
								<span style="color:green;">&#10003;</span> A contestant who has resigned from the position of Gymkhana or any representative post previously during his/her tenure is not eligible to contest for the position of a Senator (elected member of the Gymkhana).<br>
								<span style="color:green;">&#10003;</span> At the time of application, a contestant should not have been tried and/or convicted of any criminal offense or misdemeanours. The contestant must not have been proven guilty by any disciplinary committee.<br>
								<span style="color:green;">&#10003;</span> Chief Election Officer will be the final authority for any discrepancies in eligibility criteria and studentsâ€™ senate will be the appellate authority.<br>
								<span style="color:green;">&#10003;</span> The decision of the President, SGC will be binding in all cases of disputes and exceptions in these rules.<br>
								<span style="color:green;">&#10003;</span> He/she who was rejected during the general elections by the electoral body (NOTA getting the maximum votes) cannot contest for the same post at a later time.<br>
